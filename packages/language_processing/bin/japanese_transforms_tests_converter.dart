import 'dart:io';

/// A script to convert JavaScript test cases from a .js file to a Dart file.
///
/// This script reads a specific JavaScript array (`const tests = [...]`) from an
/// input file, converts the JavaScript object notation to a Dart-compatible
/// format, and writes the result to a new .dart file.
///
/// Usage:
/// dart convert_js_tests.dart `<path_to_js_input_file>` `<path_to_dart_output_file>`
void main(List<String> args) {
	if (args.length != 2) {
		print('Usage: dart convert_js_tests.dart <input.js> <output.dart>');
		exit(1);
	}

	final inputFile = File(args[0]);
	final outputFile = File(args[1]);

	if (!inputFile.existsSync()) {
		print('Error: Input file not found at ${inputFile.path}');
		exit(1);
	}

	try {
		// 1. Read the content of the JavaScript file.
		final jsContent = inputFile.readAsStringSync();

		// 2. Isolate the array content from the `const tests = [...]` declaration.
		final startMarker = 'const tests = [';
		final endMarker = '];';

		final startIndex = jsContent.indexOf(startMarker);
		final endIndex = jsContent.lastIndexOf(endMarker);

		if (startIndex == -1 || endIndex == -1) {
			print('Error: Could not find the "const tests = [...]" array in the input file.');
			exit(1);
		}

		// Extract the raw string content of the array.
		String arrayContent = jsContent.substring(startIndex + startMarker.length, endIndex);
		
		// 3. Convert JS object keys to quoted Dart map keys.
		// The JS test file uses unquoted keys (e.g., `category:`), which is invalid in Dart.
		// This regex finds keys that are not enclosed in quotes and adds single quotes around them.
		String dartMapContent = arrayContent.replaceAllMapped(
			RegExp(r'([{,]\s*)([a-zA-Z0-9_-]+):'), 
			(match) => "${match[1]}'${match[2]}':"
		);
		// Note: The rest of the JS object literal syntax (single-quoted strings, trailing commas)
		// is already valid in Dart, so no further replacements are needed.

		// 4. Format the final Dart code string.
		final dartCode = """
// Auto-generated from ${inputFile.path} by convert_js_tests.dart
// Do not edit this file manually.

final yomitanConjugateTestCases = <Map<String, dynamic>>[
$dartMapContent
];
""";
		
		// 5. Write the generated Dart code to the output file.
		outputFile.writeAsStringSync(dartCode);

		print('Successfully converted ${inputFile.path} to ${outputFile.path}');

	} catch (e) {
		print('An error occurred during conversion: $e');
		exit(1);
	}
}

